package dto;

/**
 *
 * @author acer
 */
public class OrderDetail {
    private int id;
    private int itemid;
    private int orderid;
    private int quantity;
    private int price;


    public OrderDetail(int id, int itemid, int orderid, int quantity, int price) {
        this.id = id;
        this.itemid = itemid;
        this.orderid = orderid;
        this.quantity = quantity;
        this.price = price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getItemid() {
        return itemid;
    }

    public void setItemid(int itemid) {
        this.itemid = itemid;
    }

    public int getOrderid() {
        return orderid;
    }

    public void setOrderid(int orderid) {
        this.orderid = orderid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
    
    
    
}
